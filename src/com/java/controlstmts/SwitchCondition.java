package com.java.controlstmts;

public class SwitchCondition {

	public static void main(String[] args)
	{
	//Executes upto the condition matches - break keyword.
  String name="Arjun";
  switch(name)
  {
	  case "Manasa":
		  System.out.println("Manasa executed");
	  case "Arjun":
		  System.out.println("Arjun executed");
		    break;
	  case "Babu":
		  System.out.println("Babu executed");
	  
	  
  }
  
	}

}
